// calculadora 

function somar(a,b) {
    return a + b
};  
function subtrair(a,b) {
    return a - b
};  
function multiplicar(a,b) {
    return a * b
};  
function dividir(a,b) {
    return a / b
};  

console.log(somar(5,5));
console.log(subtrair(15,5));
console.log(multiplicar(2,5));
console.log(dividir(20,2));







